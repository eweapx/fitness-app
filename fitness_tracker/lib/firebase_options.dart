        authDomain: 'fitness-tracker-app.firebaseapp.com',
        storageBucket: 'fitness-tracker-app.appspot.com',
        measurementId: 'G-MEASUREMENT_ID',
      );

  static FirebaseOptions get android => FirebaseOptions(
        apiKey: _apiKey,
        appId: '1:123456789012:android:abc123def456',
        messagingSenderId: '123456789012',
        projectId: 'fitness-tracker-app',
        storageBucket: 'fitness-tracker-app.appspot.com',
      );

  static FirebaseOptions get ios => FirebaseOptions(
        apiKey: _apiKey,
        appId: '1:123456789012:ios:abc123def456',
        messagingSenderId: '123456789012',
        projectId: 'fitness-tracker-app',
        storageBucket: 'fitness-tracker-app.appspot.com',
        iosClientId: 'ios-client-id.apps.googleusercontent.com',
